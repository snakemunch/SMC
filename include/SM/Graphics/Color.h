#ifndef CL_GRAPHICS_COLOR_H
#define CL_GRAPHICS_COLOR_H

#include <CL/Config>

namespace CL { namespace Graphics {

	class Color {
		
	public:
		
		unsigned char red;
		unsigned char green;
		unsigned char blue;
		unsigned char alpha;
		
		static Color grayColor() {
			Color c;
			c.red = 0x80;
			c.green = 0x80;
			c.blue = 0x80;
			c.alpha = 0;
			return c;
		}
		
	};
	
}}

#endif // CL_GRAPHICS_COLOR_H